package com.etl.rum.rumtestapp.RecyclerViewWithHeader;

public class ListItem {
    private String name;
    private int id;

    public ListItem() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}